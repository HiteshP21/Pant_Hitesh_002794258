public class Hospital {
    private String hopName;
    private int hospId;
    private String hospAdd;
    private String hospCom;

    public String getHopName() {
        return hopName;
    }

    public void setHopName(String hopName) {
        this.hopName = hopName;
    }

    public int getHospId() {
        return hospId;
    }

    public void setHospId(int hospId) {
        this.hospId = hospId;
    }

    public String getHospAdd() {
        return hospAdd;
    }

    public void setHospAdd(String hospAdd) {
        this.hospAdd = hospAdd;
    }

    public String getHospCom() {
        return hospCom;
    }

    public void setHospCom(String hospCom) {
        this.hospCom = hospCom;
    }
}
